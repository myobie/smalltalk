// ─── Command argv construction ─────────────────────────────────────────

describe('cmdLaunch — command argv', () => {
  it('claude direct path uses --resume with a session id', async () => {
    const r = await cmdLaunch(
      baseInput({ harness: 'claude', identity: 'alice' }),
      ctx
    );
    expect(r.argv[0]).toBe('claude');
    expect(r.argv).toContain('--resume');
    expect(r.argv).toContain('--permission-mode');
    expect(r.argv).toContain('auto');
  });

  it('codex direct path uses `codex resume <sid>` when .codex-session-id exists', async () => {
    writeFileSync(join(cwd, '.codex-session-id'), 'abc-123\n');
    const r = await cmdLaunch(baseInput({ harness: 'codex' }), ctx);
    expect(r.argv).toEqual(['codex', 'resume', 'abc-123']);
  });

  it('codex direct path is bare `codex` when no .codex-session-id', async () => {
    const r = await cmdLaunch(baseInput({ harness: 'codex' }), ctx);
    expect(r.argv).toEqual(['codex']);
  });

  it('--model routes through `ollama launch <harness> --model <spec>`', async () => {
    const r = await cmdLaunch(
      baseInput({ harness: 'claude', model: 'glm-5.2:cloud' }),
      ctx
    );
    expect(r.argv).toEqual([
      'ollama',
      'launch',
      'claude',
      '--model',
      'glm-5.2:cloud',
    ]);
    expect(r.usedOllama).toBe(true);
  });

  it('--model works for codex too', async () => {
    const r = await cmdLaunch(
      baseInput({ harness: 'codex', model: 'glm-5.2:cloud' }),
      ctx
    );
    expect(r.argv).toEqual([
      'ollama',
      'launch',
      'codex',
      '--model',
      'glm-5.2:cloud',
    ]);
    expect(r.usedOllama).toBe(true);
  });
});

