// tests/unit/launch.test.ts — brief-016 `smalltalk launch` core.
//
// Focus: identity resolution, command-argv construction, pty.toml
// content, dry-run flow. Actual harness spawning is out of scope for
// unit tests — we exercise the captureOnly/dryRun seams that return
// the constructed argv without executing anything.

import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { afterEach, beforeEach, describe, expect, it } from 'vitest';

import type { CliContext } from '../../src/cli-context.ts';
import { cmdLaunch } from '../../src/commands/launch.ts';

let scratch: string;
let cwd: string;
let coordRoot: string;
let home: string;
let stderrBuf: string;
let stdoutBuf: string;
let ctx: CliContext;

beforeEach(() => {
  scratch = mkdtempSync(join(tmpdir(), 'st-launch-'));
  cwd = join(scratch, 'repo');
  coordRoot = join(scratch, 'state');
  home = join(scratch, 'home');
  mkdirSync(cwd, { recursive: true });
  mkdirSync(coordRoot, { recursive: true });
  mkdirSync(home, { recursive: true });
  stderrBuf = '';
  stdoutBuf = '';
  ctx = {
    env: {},
    coordRoot,
    coordConfig: '/unused',
    stdout: (s) => {
      stdoutBuf += s;
    },
    stderr: (s) => {
      stderrBuf += s;
    },
    readStdin: async () => Buffer.alloc(0),
  };
});

afterEach(() => {
  rmSync(scratch, { recursive: true, force: true });
});

function baseInput(overrides: Partial<Parameters<typeof cmdLaunch>[0]> = {}) {
  return {
    harness: 'claude' as const,
    cwd,
    home,
    coordRoot,
    env: {} as NodeJS.ProcessEnv,
    dryRun: true,
    ptyBinPath: '', // treat as pty NOT on PATH
    ...overrides,
  };
}

// ─── Identity resolution ───────────────────────────────────────────────

describe('cmdLaunch — identity resolution', () => {
  it('uses --identity when set', async () => {
    const r = await cmdLaunch(baseInput({ identity: 'alice' }), ctx);
    expect(r.identity).toBe('alice');
    expect(r.identityAutoGenerated).toBe(false);
    expect(stderrBuf).not.toMatch(/anon-/);
  });

  it('falls back to $ST_AGENT when --identity is unset', async () => {
    const r = await cmdLaunch(
      baseInput({ env: { ST_AGENT: 'bob' } }),
      ctx
    );
    expect(r.identity).toBe('bob');
    expect(r.identityAutoGenerated).toBe(false);
  });

  it('falls back to $ST_IDENTITY when ST_AGENT unset (deprecation chain)', async () => {
    const r = await cmdLaunch(
      baseInput({ env: { ST_IDENTITY: 'legacy-alice' } }),
      ctx
    );
    expect(r.identity).toBe('legacy-alice');
    expect(r.identityAutoGenerated).toBe(false);
  });

  it('falls back to $COORD_IDENTITY when neither ST_* is set', async () => {
    const r = await cmdLaunch(
      baseInput({ env: { COORD_IDENTITY: 'legacy-coord-bob' } }),
      ctx
    );
    expect(r.identity).toBe('legacy-coord-bob');
    expect(r.identityAutoGenerated).toBe(false);
  });

  it('generates anon-<rand6> when no identity is available', async () => {
    const r = await cmdLaunch(baseInput({}), ctx);
    expect(r.identity).toMatch(/^anon-[0-9a-z]{6}$/);
    expect(r.identityAutoGenerated).toBe(true);
    expect(stderrBuf).toMatch(/no ST_AGENT set/);
    expect(stderrBuf).toMatch(/set ST_AGENT to persist/);
    expect(stderrBuf).toMatch(/anon-[0-9a-z]{6}/);
  });

  it('explicit --identity wins over $ST_AGENT', async () => {
    const r = await cmdLaunch(
      baseInput({ identity: 'explicit', env: { ST_AGENT: 'from-env' } }),
      ctx
    );
    expect(r.identity).toBe('explicit');
  });
});

